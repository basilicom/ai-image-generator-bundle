    public function getColorName(string $color): ?string
    {
        if (!str_starts_with($color, '#')) {
            return $color;
        }

        $color = ltrim(strtolower($color), '#');
        if (strlen($color) === 3) {
            $color = implode('', array_map(fn ($hexVal) => $hexVal . $hexVal, str_split($color)));
        }

        return $this->dictionary[$color] ?? $this->findClosestColor($color);
    }

    private function findClosestColor(string $hexCode)
    {
        $minDistance = PHP_INT_MAX;
        $closestColor = null;

        foreach ($this->dictionary as $colorHex => $colorName) {
            $distance = $this->calculateColorDistance($hexCode, $colorHex);
            if ($distance < $minDistance) {
                $minDistance = $distance;
                $closestColor = $colorName;
            }
        }

        return $closestColor;
    }

    private function calculateColorDistance($color1, $color2): float
    {
        $rgb1 = $this->hexToRgb($color1);
        $rgb2 = $this->hexToRgb($color2);

        $rDiff = $rgb1['r'] - $rgb2['r'];
        $gDiff = $rgb1['g'] - $rgb2['g'];
        $bDiff = $rgb1['b'] - $rgb2['b'];

        return sqrt($rDiff * $rDiff + $gDiff * $gDiff + $bDiff * $bDiff);
    }

    private function hexToRgb($hex): array
    {
        $hex = ltrim($hex, '#');

        return [
            'r' => hexdec(substr($hex, 0, 2)),
            'g' => hexdec(substr($hex, 2, 2)),
            'b' => hexdec(substr($hex, 4, 2))
        ];
    }

